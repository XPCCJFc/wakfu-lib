package wakfulib.utils;

/**
 * Utility class providing constant instances of empty arrays for various types.
 * Using these constants helps avoid repeated allocations of zero-length arrays.
 */
public class Empty {
    /** An empty byte array. */
    public static final byte[] EMPTY_BYTE_ARRAY = new byte[0];
    /** An empty character array. */
    public static final char[] EMPTY_CHAR_ARRAY = new char[0];
    /** An empty integer array. */
    public static final int[] EMPTY_INT_ARRAY = new int[0];
    /** An empty float array. */
    public static final float[] EMPTY_FLOAT_ARRAY = new float[0];
    /** An empty string array. */
    public static final String[] EMPTY_STRING_ARRAY = new String[0];
}
